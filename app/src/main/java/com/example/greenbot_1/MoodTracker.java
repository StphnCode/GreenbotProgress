package com.example.greenbot_1;

public class MoodTracker {
    String date;
    int moodRate;

    public MoodTracker() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getMoodRate() {
        return moodRate;
    }

    public void setMoodRate(int moodRate) {
        this.moodRate = moodRate;
    }
}
